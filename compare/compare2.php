<?php
// boolean :  데이터의 형식(또는 타입) 중 하나

// 연산자 : 어떤 작업을 컴퓨터에게 지시하기 위한 기호
// 연산자의 종류
// - 대입 연산자(=)
// - 비교 연산자 : 주어진 값들이 같은지, 다른지, 큰지, 작은지를 구분하는 것을 의미. 비교 연산자의 결과는 true/false 둘 중의 하나로 나타난다.
// - boolean : true/false로 나타나는 데이터 형

// -- 부등호 연산
echo '<h1>부등호(>) 연산</h1>';
echo '<hr/>';
echo '<h2>(>)</h2>';

echo '10 > 20 : ';
var_dump(10 > 20);
echo '<br/>';

echo '10 > 1 : ';
var_dump(10 > 1);
echo '<br/>';

echo '10 > 10 : ';
var_dump(10 > 10);
echo '<br/>';

// -- 크거나 같다
echo '<h2>(>=)</h2>';

echo '10 >= 20 : ';
var_dump(10 >= 20);
echo '<br/>';

echo '10 >= 1 : ';
var_dump(10 >= 1);
echo '<br/>';

echo '10 >= 10 : ';
var_dump(10 >= 10);
echo '<br/>';


?>


