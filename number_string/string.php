<?php
// string 테스트
echo "Hello world";
echo "<br/>";
// 숫자와 문자의 구분
// 숫자형
echo var_dump(12345);
echo "<br/>";
// 숫자와 문자의 구분
// 문자형
echo var_dump("12345");
echo "<br/>";
// 문자와 문자를 결합할 때, '.'을 사용
echo "hello"." ","world";
echo "<br/>";
// 문자열 안에서 따옴표를 사용하고 싶을 때 
// 1. '\'를 추가. escaping
echo "He said, \"hello world\"";
echo "<br/>";
// 2. ''를 사용
echo 'He said, "hello world"';
?>
